function numCheck(num) {

    if (num % 2 === 0) {
        console.log('even');
    } else {
        console.log('odd');
    }
}

numCheck(4);
numCheck(7);

function numCheck(num) {

    if (num % 2 !== 0) {
        console.log('even');
    } else {
        console.log('odd');
    }
}

numCheck(4);
numCheck(7);